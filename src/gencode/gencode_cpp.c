struct cpp_gen_param {
    sstr_t output;
    int format;
    struct hash_map* dependency_map;
    struct hash_map* oneof_map;
};

static void gen_cpp_class(struct struct_container* sc, int format, sstr_t out) {
    const char* name = sstr_cstr(sc->name);
    const char* marshal_pfix = marshal_indent_prefix(format);
    const char* unmarshal_pfix = unmarshal_prefix_for_format(format);
    int is_json = (format == 0);

    sstr_append_cstr(out, "class ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, " {\npublic:\n");

    /* Default constructor */
    sstr_append_cstr(out, "    ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "() { ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "_init(&data_); }\n");

    /* Destructor */
    sstr_append_cstr(out, "    ~");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "() { ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "_clear(&data_); }\n\n");

    /* Move constructor */
    sstr_append_cstr(out, "    ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "(");
    sstr_append_cstr(out, name);
    if (is_json) {
        sstr_append_cstr(out, "&& other) noexcept {\n"
                              "        ");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_init(&data_);\n"
                              "        (void)");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_move(&data_, &other.data_);\n"
                              "    }\n");
    } else {
        sstr_append_cstr(out, "&& other) noexcept {\n"
                              "        std::memcpy(&data_, &other.data_, sizeof(data_));\n"
                              "        std::memset(&other.data_, 0, sizeof(other.data_));\n"
                              "    }\n");
    }

    /* Move assignment */
    sstr_append_cstr(out, "    ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "& operator=(");
    sstr_append_cstr(out, name);
    if (is_json) {
        sstr_append_cstr(out, "&& other) noexcept {\n"
                              "        if (this != &other) {\n"
                              "            (void)");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_move(&data_, &other.data_);\n"
                              "        }\n"
                              "        return *this;\n"
                              "    }\n\n");
    } else {
        sstr_append_cstr(out, "&& other) noexcept {\n"
                              "        if (this != &other) {\n"
                              "            ");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_clear(&data_);\n"
                              "            std::memcpy(&data_, &other.data_, sizeof(data_));\n"
                              "            std::memset(&other.data_, 0, sizeof(other.data_));\n"
                              "        }\n"
                              "        return *this;\n"
                              "    }\n\n");
    }

    /* Copy constructor */
    sstr_append_cstr(out, "    ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "(const ");
    sstr_append_cstr(out, name);
    if (is_json) {
        sstr_append_cstr(out, "& other) {\n"
                              "        ");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_init(&data_);\n"
                              "        if (");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_copy(&data_, &other.data_) != 0) {\n"
                              "            throw std::runtime_error(\"copy failed\");\n"
                              "        }\n"
                              "    }\n");
    } else {
        sstr_append_cstr(out, "& other) {\n"
                              "        ");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_init(&data_);\n"
                              "        sstr_t buf = sstr_new();\n"
                              "        ");
        sstr_append_cstr(out, marshal_pfix);
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "(const_cast<struct ::");
        sstr_append_cstr(out, name);
        if (format == 0) {
            sstr_append_cstr(out, "*>(&other.data_), 0, 0, buf);\n");
        } else {
            sstr_append_cstr(out, "*>(&other.data_), buf);\n");
        }
        sstr_append_cstr(out, "        ");
        sstr_append_cstr(out, unmarshal_pfix);
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "(buf, &data_);\n"
                              "        sstr_free(buf);\n"
                              "    }\n");
    }

    /* Copy assignment */
    sstr_append_cstr(out, "    ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "& operator=(const ");
    sstr_append_cstr(out, name);
    if (is_json) {
        sstr_append_cstr(out, "& other) {\n"
                              "        if (this != &other) {\n"
                              "            if (");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_copy(&data_, &other.data_) != 0) {\n"
                              "                throw std::runtime_error(\"copy failed\");\n"
                              "            }\n"
                              "        }\n"
                              "        return *this;\n"
                              "    }\n\n");
    } else {
        sstr_append_cstr(out, "& other) {\n"
                              "        if (this != &other) {\n"
                              "            ");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_clear(&data_);\n"
                              "            ");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "_init(&data_);\n"
                              "            sstr_t buf = sstr_new();\n"
                              "            ");
        sstr_append_cstr(out, marshal_pfix);
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "(const_cast<struct ::");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "*>(&other.data_), buf);\n");
        sstr_append_cstr(out, "            ");
        sstr_append_cstr(out, unmarshal_pfix);
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "(buf, &data_);\n"
                              "            sstr_free(buf);\n"
                              "        }\n"
                              "        return *this;\n"
                              "    }\n\n");
    }

    /* ---- Field accessors ---- */
    sstr_append_cstr(out, "    // Field accessors\n");
    struct struct_field* f = sc->fields;
    while (f) {
        emit_field_accessors(out, f, name);
        f = f->next;
    }
    sstr_append_cstr(out, "\n");

    /* ---- marshal() ---- */
    sstr_append_cstr(out, "    /// Serialize to string.\n"
                          "    std::string marshal() const {\n"
                          "        sstr_t buf = sstr_new();\n"
                          "        ");
    sstr_append_cstr(out, marshal_pfix);
    sstr_append_cstr(out, name);
    if (is_json) {
        sstr_append_cstr(out, "(const_cast<struct ::");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "*>(&data_), 0, 0, buf);\n");
    } else {
        sstr_append_cstr(out, "(const_cast<struct ::");
        sstr_append_cstr(out, name);
        sstr_append_cstr(out, "*>(&data_), buf);\n");
    }
    sstr_append_cstr(out,
        "        std::string result(sstr_cstr(buf), sstr_length(buf));\n"
        "        sstr_free(buf);\n"
        "        return result;\n"
        "    }\n\n");

    /* ---- unmarshal() static factory ---- */
    sstr_append_cstr(out, "    /// Deserialize from string. Throws on error.\n"
                          "    static ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, " unmarshal(const std::string& data) {\n"
                          "        ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, " obj;\n"
                          "        sstr_t in = sstr_of(data.data(), data.size());\n"
                          "        int rc = ");
    sstr_append_cstr(out, unmarshal_pfix);
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "(in, &obj.data_);\n"
                          "        sstr_free(in);\n"
                          "        if (rc != 0) throw std::runtime_error(\"unmarshal failed\");\n"
                          "        return obj;\n"
                          "    }\n\n");

    /* ---- unmarshal_into() ---- */
    sstr_append_cstr(out, "    /// Deserialize into this object. Returns true on success.\n"
                          "    bool unmarshal_into(const std::string& data) {\n"
                          "        sstr_t in = sstr_of(data.data(), data.size());\n"
                          "        int rc = ");
    sstr_append_cstr(out, unmarshal_pfix);
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "(in, &data_);\n"
                          "        sstr_free(in);\n"
                          "        return rc == 0;\n"
                          "    }\n\n");

    /* ---- C interop ---- */
    sstr_append_cstr(out, "    /// Access the underlying C struct.\n"
                          "    struct ::");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "& c_struct() { return data_; }\n"
                          "    const struct ::");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "& c_struct() const { return data_; }\n\n");

    /* ---- Equality via marshal comparison ---- */
    sstr_append_cstr(out, "    bool operator==(const ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "& other) const { return marshal() == other.marshal(); }\n");
    sstr_append_cstr(out, "    bool operator!=(const ");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, "& other) const { return !(*this == other); }\n\n");

    /* private data */
    sstr_append_cstr(out, "private:\n"
                          "    struct ::");
    sstr_append_cstr(out, name);
    sstr_append_cstr(out, " data_;\n"
                          "};\n\n");
}

/* ========================================================================= */
/* Dependency-ordered struct iteration (same pattern as gencode.c)           */
/* ========================================================================= */

static void do_each_cpp_struct(void* key, void* value, void* ptr) {
    sstr_t k = (sstr_t)key;
    struct struct_container* sc = (struct struct_container*)value;
    struct cpp_gen_param* param = (struct cpp_gen_param*)ptr;
    struct hash_map* dep_map = param->dependency_map;

    void* dv = NULL;
    if (hash_map_find(dep_map, k, &dv) == HASH_MAP_OK) return;

    /* Check struct-typed field dependencies */
    struct struct_field* f = sc->fields;
    while (f) {
        if (f->type == FIELD_TYPE_STRUCT) {
            if (hash_map_find(dep_map, f->type_name, &dv) != HASH_MAP_OK)
                return;
        }
        if (f->type == FIELD_TYPE_MAP && f->map_value_type == FIELD_TYPE_STRUCT) {
            if (hash_map_find(dep_map, f->map_value_type_name, &dv) != HASH_MAP_OK)
                return;
        }
        if (f->type == FIELD_TYPE_ONEOF) {
            if (hash_map_find(dep_map, f->type_name, &dv) != HASH_MAP_OK)
                return;
        }
        f = f->next;
    }

    gen_cpp_class(sc, param->format, param->output);
    hash_map_insert(dep_map, sstr_dup(k), NULL);
}

/* Oneofs also need forward-declaration handling: emit after variant structs */
static void do_each_cpp_oneof(void* key, void* value, void* ptr) {
    sstr_t k = (sstr_t)key;
    struct oneof_container* oc = (struct oneof_container*)value;
    struct cpp_gen_param* param = (struct cpp_gen_param*)ptr;
    struct hash_map* dep_map = param->dependency_map;

    void* dv = NULL;
    if (hash_map_find(dep_map, k, &dv) == HASH_MAP_OK) return;

    /* Ensure all variant struct types are emitted first */
    struct oneof_variant* v = oc->variants;
    while (v) {
        if (hash_map_find(dep_map, v->struct_type_name, &dv) != HASH_MAP_OK)
            return;
        v = v->next;
    }

    /* Oneofs don't get their own wrapper class - they use the generated
       C tagged union directly. Just mark as resolved. */
    hash_map_insert(dep_map, sstr_dup(k), NULL);
}

static void cpp_dummy_free(void* ptr) { (void)ptr; }

/* ========================================================================= */
/* Public entry point                                                        */
/* ========================================================================= */

int gencode_cpp_wrapper(struct hash_map* struct_map, struct hash_map* enum_map,
                        struct hash_map* oneof_map, const char* c_header_name,
                        int format, sstr_t output) {
    (void)enum_map; /* enums are directly usable from C, no wrapping needed */

    struct hash_map* dep_map =
        hash_map_new(256, sstr_key_hash, sstr_key_cmp, sstr_key_free,
                     cpp_dummy_free);
    if (dep_map == NULL) return -1;

    emit_header(output, c_header_name);

    struct cpp_gen_param param;
    param.output = output;
    param.format = format;
    param.dependency_map = dep_map;
    param.oneof_map = oneof_map;

    int total = struct_map->size + oneof_map->size;
    for (int i = 0; i < total; ++i) {
        hash_map_for_each(struct_map, do_each_cpp_struct, &param);
        hash_map_for_each(oneof_map, do_each_cpp_oneof, &param);
        if (dep_map->size >= total) break;
    }

    if (dep_map->size < total) {
        hash_map_free(dep_map);
        return -1;
    }

    emit_footer(output);
    hash_map_free(dep_map);
    return 0;
}
