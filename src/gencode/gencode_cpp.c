/**
 * @file gencode_cpp.c
 * @brief Generate a C++ wrapper header (.gen.hpp) providing RAII classes
 * around the generated C structs, with typed accessors and marshal/unmarshal
 * member functions.
 */

#include "gencode/gencode.h"
#include "struct/struct_parse.h"
#include "utils/hash_map.h"
#include "utils/sstr.h"

#include <stdio.h>
#include <string.h>

/* Wire-format prefixes for marshal/unmarshal function names. */

static const char* unmarshal_prefix_for_format(int format) {
    switch (format) {
        case 1:  return "msgpack_unpack_";
        case 2:  return "cbor_unpack_";
        default: return "json_unmarshal_";
    }
}

static const char* marshal_indent_prefix(int format) {
    switch (format) {
        case 1:  return "msgpack_pack_";
        case 2:  return "cbor_pack_";
        default: return "json_marshal_indent_";
    }
}

/* Return the C++ type string for a getter return type. */
static const char* cpp_getter_type(int field_type) {
    switch (field_type) {
        case FIELD_TYPE_INT:    return "int";
        case FIELD_TYPE_LONG:   return "long";
        case FIELD_TYPE_FLOAT:  return "float";
        case FIELD_TYPE_DOUBLE: return "double";
        case FIELD_TYPE_BOOL:   return "bool";
        case FIELD_TYPE_SSTR:   return "std::string";
        case FIELD_TYPE_INT8:   return "int8_t";
        case FIELD_TYPE_INT16:  return "int16_t";
        case FIELD_TYPE_INT32:  return "int32_t";
        case FIELD_TYPE_INT64:  return "int64_t";
        case FIELD_TYPE_UINT8:  return "uint8_t";
        case FIELD_TYPE_UINT16: return "uint16_t";
        case FIELD_TYPE_UINT32: return "uint32_t";
        case FIELD_TYPE_UINT64: return "uint64_t";
        default:                return NULL;
    }
}

static int is_scalar_type(int field_type) {
    return (field_type >= FIELD_TYPE_INT && field_type <= FIELD_TYPE_DOUBLE)
        || field_type == FIELD_TYPE_BOOL
        || (field_type >= FIELD_TYPE_INT8 && field_type <= FIELD_TYPE_UINT64);
}

/* ========================================================================= */
/* Header / footer                                                           */
/* ========================================================================= */

static void emit_header(sstr_t out, const char* c_header_name) {
    sstr_append_cstr(out,
        "// Auto-generated C++ wrapper header — DO NOT EDIT.\n"
        "// Generated by json-gen-c with --cpp-wrapper.\n"
        "#pragma once\n\n"
        "#include <string>\n"
        "#include <cstring>\n"
        "#include <stdexcept>\n"
        "#include <utility>\n"
        "#include <vector>\n\n");
    sstr_append_cstr(out, "#include \"");
    sstr_append_cstr(out, c_header_name);
    sstr_append_cstr(out, "\"\n\n");
    sstr_append_cstr(out, "namespace jgc {\n\n");
}

static void emit_footer(sstr_t out) {
    sstr_append_cstr(out, "} // namespace jgc\n");
}

/* ========================================================================= */
/* Per-field accessor generation                                             */
/* ========================================================================= */

static void emit_scalar_accessors(sstr_t out, struct struct_field* f,
                                  const char* struct_name) {
    const char* ctype = cpp_getter_type(f->type);
    const char* fname = sstr_cstr(f->name);
    (void)struct_name;

    /* getter */
    sstr_append_cstr(out, "    ");
    sstr_append_cstr(out, ctype);
    sstr_append_cstr(out, " ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");

    /* setter */
    sstr_append_cstr(out, "    void set_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "(");
    sstr_append_cstr(out, ctype);
    sstr_append_cstr(out, " v) { data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " = v; }\n");
}

static void emit_bool_accessors(sstr_t out, struct struct_field* f,
                                const char* struct_name) {
    const char* fname = sstr_cstr(f->name);
    (void)struct_name;

    /* In C, bool fields may be emitted as int; cast for safety */
    sstr_append_cstr(out, "    bool ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return static_cast<bool>(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "); }\n");

    sstr_append_cstr(out, "    void set_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "(bool v) { data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " = v; }\n");
}

static void emit_string_accessors(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);

    /* getter returns std::string; handles NULL sstr_t from _init */
    sstr_append_cstr(out, "    std::string ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const {\n"
                          "        if (!data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, ") return std::string();\n"
                          "        return std::string(sstr_cstr(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "), sstr_length(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "));\n    }\n");

    /* setter from std::string; creates sstr_t if NULL */
    sstr_append_cstr(out, "    void set_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "(const std::string& v) {\n"
                          "        if (!data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, ") data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " = sstr_new();\n"
                          "        sstr_clear(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, ");\n"
                          "        sstr_append_of(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, ", v.data(), v.size());\n    }\n");

    /* setter from C string; creates sstr_t if NULL */
    sstr_append_cstr(out, "    void set_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "(const char* v) {\n"
                          "        if (!data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, ") data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " = sstr_new();\n"
                          "        sstr_clear(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, ");\n"
                          "        sstr_append_cstr(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, ", v);\n    }\n");
}

static void emit_enum_accessors(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);
    const char* tname = sstr_cstr(f->type_name);

    sstr_append_cstr(out, "    enum ");
    sstr_append_cstr(out, tname);
    sstr_append_cstr(out, " ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return static_cast<enum ");
    sstr_append_cstr(out, tname);
    sstr_append_cstr(out, ">(data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "); }\n");

    sstr_append_cstr(out, "    void set_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "(enum ");
    sstr_append_cstr(out, tname);
    sstr_append_cstr(out, " v) { data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " = v; }\n");
}

static void emit_struct_accessors(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);
    const char* tname = sstr_cstr(f->type_name);

    /* mutable C struct reference — use :: to escape jgc namespace */
    sstr_append_cstr(out, "    struct ::");
    sstr_append_cstr(out, tname);
    sstr_append_cstr(out, "& ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");

    /* const C struct reference */
    sstr_append_cstr(out, "    const struct ::");
    sstr_append_cstr(out, tname);
    sstr_append_cstr(out, "& ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");
}

/* Return the C pointer type string for array element access.
   In the generated C code, bool and enum are stored as int. */
static const char* c_array_element_type(int field_type, const char* type_name) {
    switch (field_type) {
        case FIELD_TYPE_INT:    return "int*";
        case FIELD_TYPE_LONG:   return "long*";
        case FIELD_TYPE_FLOAT:  return "float*";
        case FIELD_TYPE_DOUBLE: return "double*";
        case FIELD_TYPE_BOOL:   return "int*";   /* C stores bool as int */
        case FIELD_TYPE_ENUM:   return "int*";   /* C stores enum as int */
        case FIELD_TYPE_SSTR:   return "sstr_t*";
        case FIELD_TYPE_INT8:   return "int8_t*";
        case FIELD_TYPE_INT16:  return "int16_t*";
        case FIELD_TYPE_INT32:  return "int32_t*";
        case FIELD_TYPE_INT64:  return "int64_t*";
        case FIELD_TYPE_UINT8:  return "uint8_t*";
        case FIELD_TYPE_UINT16: return "uint16_t*";
        case FIELD_TYPE_UINT32: return "uint32_t*";
        case FIELD_TYPE_UINT64: return "uint64_t*";
        default:                return NULL; /* struct/map handled separately */
    }
    (void)type_name;
}

static void emit_dynamic_array_accessors(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);
    const char* ptr_type = c_array_element_type(f->type, sstr_cstr(f->type_name));

    if (ptr_type) {
        sstr_append_cstr(out, "    ");
        sstr_append_cstr(out, ptr_type);
        sstr_append_cstr(out, " ");
    } else if (f->type == FIELD_TYPE_STRUCT) {
        sstr_append_cstr(out, "    struct ::");
        sstr_append_cstr(out, sstr_cstr(f->type_name));
        sstr_append_cstr(out, "* ");
    } else {
        sstr_append_cstr(out, "    void* ");
    }
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "_data() { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");

    /* .field_len() → length */
    sstr_append_cstr(out, "    int ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "_len() const { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "_len; }\n");
}

static void emit_fixed_array_accessors(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);
    const char* ptr_type = c_array_element_type(f->type, sstr_cstr(f->type_name));
    char size_str[32];
    snprintf(size_str, sizeof(size_str), "%d", f->array_size);

    /* .field_data() → pointer to first element */
    if (ptr_type) {
        sstr_append_cstr(out, "    ");
        sstr_append_cstr(out, ptr_type);
        sstr_append_cstr(out, " ");
    } else if (f->type == FIELD_TYPE_STRUCT) {
        sstr_append_cstr(out, "    struct ::");
        sstr_append_cstr(out, sstr_cstr(f->type_name));
        sstr_append_cstr(out, "* ");
    } else {
        sstr_append_cstr(out, "    void* ");
    }
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "_data() { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");

    /* .field_size() → compile-time size */
    sstr_append_cstr(out, "    static constexpr int ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "_size() { return ");
    sstr_append_cstr(out, size_str);
    sstr_append_cstr(out, "; }\n");
}

static void emit_map_accessors(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);
    /* Map fields have C struct types like json_map_int, json_map_sstr_t, etc.
       The C struct name depends on the actual storage type, not the schema type.
       bool and enum are stored as int, so their maps use json_map_int. */
    const char* c_map_suffix;
    switch (f->map_value_type) {
        case FIELD_TYPE_INT:    c_map_suffix = "int"; break;
        case FIELD_TYPE_LONG:   c_map_suffix = "long"; break;
        case FIELD_TYPE_FLOAT:  c_map_suffix = "float"; break;
        case FIELD_TYPE_DOUBLE: c_map_suffix = "double"; break;
        case FIELD_TYPE_BOOL:   c_map_suffix = "int"; break;
        case FIELD_TYPE_ENUM:   c_map_suffix = "int"; break;
        case FIELD_TYPE_SSTR:   c_map_suffix = "sstr_t"; break;
        case FIELD_TYPE_INT8:   c_map_suffix = "int8_t"; break;
        case FIELD_TYPE_INT16:  c_map_suffix = "int16_t"; break;
        case FIELD_TYPE_INT32:  c_map_suffix = "int32_t"; break;
        case FIELD_TYPE_INT64:  c_map_suffix = "int64_t"; break;
        case FIELD_TYPE_UINT8:  c_map_suffix = "uint8_t"; break;
        case FIELD_TYPE_UINT16: c_map_suffix = "uint16_t"; break;
        case FIELD_TYPE_UINT32: c_map_suffix = "uint32_t"; break;
        case FIELD_TYPE_UINT64: c_map_suffix = "uint64_t"; break;
        case FIELD_TYPE_STRUCT: c_map_suffix = sstr_cstr(f->map_value_type_name); break;
        default:                c_map_suffix = sstr_cstr(f->map_value_type_name); break;
    }

    sstr_append_cstr(out, "    struct ::json_map_");
    sstr_append_cstr(out, c_map_suffix);
    sstr_append_cstr(out, "& ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");

    sstr_append_cstr(out, "    const struct ::json_map_");
    sstr_append_cstr(out, c_map_suffix);
    sstr_append_cstr(out, "& ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");
}

static void emit_oneof_accessors(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);
    const char* tname = sstr_cstr(f->type_name);

    /* .field() → mutable reference — use :: to escape jgc namespace */
    sstr_append_cstr(out, "    struct ::");
    sstr_append_cstr(out, tname);
    sstr_append_cstr(out, "& ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");

    sstr_append_cstr(out, "    const struct ::");
    sstr_append_cstr(out, tname);
    sstr_append_cstr(out, "& ");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return data_.");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "; }\n");
}

static void emit_optional_helpers(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);

    sstr_append_cstr(out, "    bool has_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return data_.has_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " != 0; }\n");

    sstr_append_cstr(out, "    void set_has_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "(bool v) { data_.has_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " = v; }\n");
}

static void emit_nullable_helpers(sstr_t out, struct struct_field* f) {
    const char* fname = sstr_cstr(f->name);

    /* Nullable fields also use has_<field> in the C struct. When the field
       is nullable-only (not optional), has_<field> == false means null. */
    sstr_append_cstr(out, "    bool has_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, "() const { return data_.has_");
    sstr_append_cstr(out, fname);
    sstr_append_cstr(out, " != 0; }\n");
}

/* Emit all accessors for a single field. */
static void emit_field_accessors(sstr_t out, struct struct_field* f,
                                 const char* struct_name) {
    if (f->is_array && f->array_size > 0) {
        emit_fixed_array_accessors(out, f);
    } else if (f->is_array) {
        emit_dynamic_array_accessors(out, f);
    } else if (f->type == FIELD_TYPE_SSTR) {
        emit_string_accessors(out, f);
    } else if (f->type == FIELD_TYPE_BOOL) {
        emit_bool_accessors(out, f, struct_name);
    } else if (f->type == FIELD_TYPE_ENUM) {
        emit_enum_accessors(out, f);
    } else if (f->type == FIELD_TYPE_STRUCT) {
        emit_struct_accessors(out, f);
    } else if (f->type == FIELD_TYPE_MAP) {
        emit_map_accessors(out, f);
    } else if (f->type == FIELD_TYPE_ONEOF) {
        emit_oneof_accessors(out, f);
    } else if (is_scalar_type(f->type)) {
        emit_scalar_accessors(out, f, struct_name);
    }

    if (f->is_optional) {
        emit_optional_helpers(out, f);
    } else if (f->is_nullable) {
        /* Only emit has_ if not already emitted by optional */
        emit_nullable_helpers(out, f);
    }
}

